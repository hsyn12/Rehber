package com.tr.hsyn.telefonrehberi.main.call.activity.random.data;


public class GeneratedCalls {

    private final long date;
    private final int  size;

    public GeneratedCalls(long date, int size) {

        this.date = date;
        this.size = size;
    }

    public long getDate() {

        return date;
    }

    public int getSize() {

        return size;
    }
}
