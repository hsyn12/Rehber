package com.tr.hsyn.telefonrehberi.main.contact.comment.topics;


/**
 * The <code>Topic</code> enum defines the possible topics
 * that can be used in the contact comments.
 */
public enum Topic {
	
	/**
	 * Topic about call quantity
	 */
	QUANTITY,
	/**
	 * Topic about last call
	 */
	LAST_CALL,
	/**
	 * Topic about call history duration
	 */
	HISTORY_DURATION,
	/**
	 * Topic about call duration
	 */
	CALL_DURATION
}
